operadorA = 10
operadorB = 14

suma = operadorA + operadorB
#print("Resultado suma: ", suma)
print(f'Resultado suma: {suma}')

# Resta

resta = operadorA - operadorB
print(f'Resultado Restoa: {resta}')

# Multiplicación
multi = operadorA * operadorB
print(f'Resultado Multiplicación {multi}')

# División
division = operadorA / operadorB
print(f'Resultado División: {division}')

# División  ENTERO
divisionint = operadorA // operadorB
print(f'Resultado Division (INT):  {divisionint}')

# Módulo
modulo = operadorA % operadorB
print(f'Resultado Módulo: {modulo}')

# Exponente
exponente = operadorA ** operadorB
print(f'Resultado Exponente {exponente}')